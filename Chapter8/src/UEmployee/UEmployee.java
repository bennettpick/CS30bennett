package UEmployee;
public class UEmployee {

	private String name;
	private double salary;
	
	//
	
	
	
	
	public UEmployee(String N, double S) {
		name = N;
		salary = S;
	}
	
	
	
	public String getName() {
		return name;
	}
	
	public double getSalary() {
		return salary;
	}
	
	
	
	
	
}

